public class Bike {
    private int gears;
    private int speed;

    public Bike(int gears, int speed) {
        this.gears = gears;
        this.speed = speed;
    }

    public int getGears() {
        return gears;
    }

    public void setGears(int gears) {
        this.gears = gears;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }
}